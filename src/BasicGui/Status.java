package BasicGui;



public class Status {
	private static String function;
	private static String doWhileCondition;
	

	static void setDoWhileCondition(String s)
	{
		doWhileCondition = s;
	}
	static String getDoWhileCondition()
	{
		return doWhileCondition;
	}
		
	static void setFunction(String s)
	{
		function =s;
	}
	static String getFunction()
	{
		return function;
	}
	
	
	

}
